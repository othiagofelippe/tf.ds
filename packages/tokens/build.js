async function cleanDist() {
  const { rm } = await import("fs/promises")
  await rm("dist", { recursive: true, force: true })
}

async function main() {
  console.log("Building @tfds/tokens...")

  await validateTokenFiles()
  await cleanDist()
  await buildPrimitives()
  for (const theme of themes) {
    await buildTheme(theme)
    await buildThemeJson(theme)
  }
  await buildTypes()
  await buildIndex()
  await buildTailwindPreset()

  console.log("Done!")
}

const isMain = process.argv[1] && import.meta.url === `file://${process.argv[1]}`

if (isMain) {
  main().catch((err) => {
    console.error(err)
    process.exit(1)
  })
}

export { main, themes, primitiveFiles, buildThemeJson, flattenTokenNames, validateTokenFiles }
